// Penjumlahan
const angka1 = 10;
const angka2 = 5;
const hasilPenjumlahan = angka1 + angka2;
console.log(`Hasil penjumlahan: ${hasilPenjumlahan}`);

// Pengurangan
const hasilPengurangan = angka1 - angka2;
console.log(`Hasil pengurangan: ${hasilPengurangan}`);

// Perkalian
const hasilPerkalian = angka1 * angka2;
console.log(`Hasil perkalian: ${hasilPerkalian}`);

// Pembagian
const hasilPembagian = angka1 / angka2;
console.log(`Hasil pembagian: ${hasilPembagian}`);

// Sisa bagi (modulo)
const hasilModulo = angka1 % angka2;
console.log(`Sisa bagi: ${hasilModulo}`);
